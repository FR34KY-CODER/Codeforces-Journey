using namespace std;

#define lli long long int

void Logic()
{
    lli n;
    cin >> n;
    n--;
    vector<lli> a1(n), a2(n+1) ;
    for (lli i = 0; i < n; i++)
    { 
        cin >> a1[i];
    }
    a2[ 0] = a1[ 0];
    a2[ n] = a1[ n - 1];
    
    for (lli i = 0; i < n - 1; i++) 
    {
        a2[ i + 1] = min(a1[ i], a1[ i + 1]);
    }

    for(lli i = 0; i < n + 1; i++) 
    {
        cout << a2[ i] << " ";
    }
    cout << endl;
}

int main()
{
    ios_base::sync_with_stdio(false); cin.tie(0); cout.tie (0);
    lli No_cases;
    cin >> No_cases;
    while( No_cases--)
    {
        Logic ();
    }
return 0;
}
