vector<vector<int>> spiral_fill(int n) {
    vector<vector<int>> grid(n, vector<int>(n, -1));
    vector<int> dx = {0, 1, 0, -1};  // right, down, left, up
    vector<int> dy = {1, 0, -1, 0};

    int x = n / 2, y = n / 2;
    if (n % 2 == 0) {
        x--;
        y--;
    }

    int val = 0;
    grid[x][y] = val++;
    int steps = 1;

    while (val < n * n) {
        for (int dir = 0; dir < 4; ++dir) {
            int len = steps + (dir / 2);
            for (int i = 0; i < len; ++i) {
                x += dx[dir];
                y += dy[dir];
                if (x >= 0 && x < n && y >= 0 && y < n && grid[x][y] == -1) {
                    grid[x][y] = val++;
                    if (val >= n * n) break;
                }
            }
            if (val >= n * n) break;
        }
        steps += 2;
    }

    return grid;
}

