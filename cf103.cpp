void solve(){
    int n;
    cin>>n;
    vector<int> x(n), y(n);
    for(int i=0;i<n;i++){
        cin>>x[i]>>y[i];
        //x + y stored as it forms pattern diagonally and they both can't be the bulb treasure
        y[i] += x[i];
    }

    sort(x.begin(), x.end());
    sort(y.begin(), y.end());

    //x and y are sorted so we can check for odd and even pairs
    int X = -1, Y = -1;
    //check for even pairs cause if same they both can't be treasure (limb points)
    //Different is suspicious
    for(int i = 0; i < n; i+=2){
        if(i == n - 1 || x[i] != x[i+1]){ // i == n-1 case checks if the loop doesn't find any different pair then last element is the answer
            X = x[i];
            break;
        }
    }
    //check for odd pairs cause if same they both can't be treasure (limb points)
    for(int i = 0; i < n; i+=2){
        if(i == n - 1 || y[i] != y[i+1]){
            Y = y[i];
            break;
        }
    }
    cout << X << " " << Y - X << endl;

}

