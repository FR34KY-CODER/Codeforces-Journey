bool isPrime(lli n){
    if(n < 2){
        return false;
    }
    if(n == 2 || n == 3){
        return true;
    }
    if(n % 2 == 0){
        return false;
    }
    for(lli i = 3; i*i <= n; i+=2){
        if(n%i == 0){
            return false;
        }
    }
    return true;
}

