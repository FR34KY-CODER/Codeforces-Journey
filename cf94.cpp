lli fillNumber(lli n, lli x, lli y){
    //base case
    if(n == 1){
        if(x == 1 && y == 1)return 1;
        if(x == 2 && y == 2)return 2;
        if(x == 2 && y == 1)return 3;
        if(x == 1 && y == 2)return 4;
    }
    lli half = 1LL << (n - 1);
    lli blockSize = 1LL << (2* (n - 1));

    if(x <= half && y <= half){
        return fillNumber(n - 1, x, y);
    } 
    else if(x > half && y > half){
        return fillNumber(n - 1, x - half, y - half) + blockSize;
    } 
    else if(x > half && y <= half){ 
        return fillNumber(n - 1, x - half, y) + 2 * blockSize;
    }
    else{
        return fillNumber(n - 1, x, y - half) + 3 * blockSize;
    }
}

pair<lli, lli> findCoordinates(int n, lli d) {
    //base case
    if(n == 1){
        if(d == 1){
             return {1, 1};
        }
        if(d == 2){
            return {2, 2};
        }
        if(d == 3){
            return {2, 1};
        }
        if(d == 4){
            return {1, 2};
        }
    }
    lli half = 1LL << (n - 1);
    lli blockSize = 1LL << (2 * (n - 1));
    
    if(d <= blockSize){
        auto c = findCoordinates(n - 1, d);
        return {c.first, c.second};
    } 
    else if(d <= 2 * blockSize){
        auto c = findCoordinates(n - 1, d - blockSize);
        return {c.first + half, c.second + half};
    } else if(d <= 3 * blockSize) {
        auto c = findCoordinates(n - 1, d - 2 * blockSize);
        return {c.first + half, c.second};
    } else {
        auto c = findCoordinates(n - 1, d - 3 * blockSize);
        return {c.first, c.second + half};
    }
}




