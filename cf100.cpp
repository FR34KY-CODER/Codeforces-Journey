void solve(int q, deque<lli> &dq, lli S, lli R, lli n){
    bool rev = false;
    while(q--){
        int s;
        cin >> s;
        if(s == 1){
            lli x;
            if(!rev){
                x = dq.back(); dq.pop_back();
                dq.push_front(x);
            }
            else{
                x = dq.front(); dq.pop_front();
                dq.push_back(x);
            }
            R = R - x * n + S;
        } 
        else if(s == 2){
            rev = !rev;
            R = (n + 1) * S - R;
        } else {
            lli k;
            cin >> k;
            if(!rev){
                dq.push_back(k);
            } else {
                dq.push_front(k);
            }
            n++;
            S += k;
            R += k * n;
        }
        cout << R << endl;
    }
}

