#include <bits/stdc++.h>
#include <iostream>

using namespace std;

int main()
{
    int horses;
    cin >> horses;
    while (horses--)
    {
        int num, count = 0, maximum = 0;
        cin >> num;
        int r[num];
        for (int i = 0; i < num; i++)
            cin >> r[i];
        for (int i = 0; i < num; i++)
        {
            if (r[i] == 0)
            {
                count++;
                maximum = max(maximum, count);
            }
            else
                count = 0;
        }
        cout << maximum << endl;
    }
    return 0;
}
