using namespace std;
#define SpeedBrrrrrrr ios_base::sync_with_stdio(false); cin.tie(NULL);
 

void solve(){
    int n, m;
    cin >> n >> m;
    
    vector<vector<int>> grid(n, vector<int>(m));
    int maxi = INT_MIN;
    
    for (int i = 0; i < n; ++i){
        for (int j = 0; j < m; ++j){
            cin >> grid[i][j];
            maxi = max(maxi, grid[i][j]);
        }
    }

    vector<int> rowCnt(n, 0), colCnt(m, 0);
    int total = 0;

    for (int i = 0; i < n; ++i){
        for (int j = 0; j < m; ++j){
            if (grid[i][j] == maxi){
                rowCnt[i]++;
                colCnt[j]++;
                total++;
            }
        }
    }
    for (int i = 0; i < n; ++i){
        for (int j = 0; j < m; ++j) {
            int temp = rowCnt[i] + colCnt[j];
            if (grid[i][j] == maxi) temp--; 
            if (temp == total){
                cout << maxi - 1 << endl;
                return;
            }
        }
    }

    cout << maxi <<endl;
}

