void Logic()
{
    lli n;
    cin >> n;
    vector<vector<int>> Output(2, vector<int>(n));
    lli Reddy = 2 * n;
    Output[0][0] = Reddy;
    Output[1][n - 1] = Reddy - 1;
    int front = 1, back = Reddy - 2;
    for (lli i = 0; i < n - 1; i++)
    {
        if (i & 1)
        {
            Output[1][i] = back - 1;
            Output[0][i + 1] = back;
            back -= 2;
        }
        else
        {
            Output[1][i] = front;
            Output[0][i + 1] = front + 1;
            front += 2;
        }
    }
    for (lli i = 0; i < 2; i++)
    {
        for (lli j = 0; j < n; j++)
        cout << Output[i][j] << " ";
        cout << endl;
    }
}

